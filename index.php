	<h2 align="center">Introducción a PHP y las estructuras de programación</h2> 
	<p>
		PHP (Hypertext Preprocessor) es un lenguaje de programación de código abierto ampliamente utilizado para el desarrollo web PHP se ejecuta en el servidor y puede generar contenido dinámico en las páginas web. Algunas de las características de PHP incluyen la capacidad de interactuar con bases de datos, el manejo de formularios, y la gestión de archivos, entre otras.
	</p>
	<p>
		Las estructuras de programación en PHP son similares a las de otros lenguajes de programación. Estas estructuras permiten controlar el flujo de la ejecución del código y realizar tareas específicas de manera eficiente. Las principales estructuras de programación en PHP son:
	</p>

	<ul>
		<li><strong>Secuenciales:</strong> Las instrucciones se ejecutan de manera secuencial, una tras otra.</li> 
		<li><strong>Condicionales:</strong> Permiten tomar decisiones y ejecutar diferentes bloques de código según se cumplan ciertas condiciones. Ejemplos de estructuras condicionales son `if`, `else`, `elseif` y `switch`.</li>
		<li><strong>Bucles: </strong> Permiten ejecutar un bloque de código varias veces, según se cumplan ciertas condiciones. Ejemplos de bucles son `for`, `while' y 'do-while`.</li>
	</ul>
	<p>
		Estas estructuras de programación se pueden combinar de diversas formas para crear algoritmos y soluciones más complejas y eficientes.
	</p>

